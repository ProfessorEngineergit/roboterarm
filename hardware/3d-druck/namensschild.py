PX = 2.4       # Pixelkantenlänge (mm)
BASE = 2.0     # Plattendicke (mm)
RAISE = 1.2    # Höhe der erhabenen Buchstaben (mm)
RAND = 4.0     # Rand um den Text (mm)
SINK = 0.4     # Buchstaben minimal in die Platte versenken (sauberer Druck)


def _box(x, y, z, dx, dy, dz):
    p = [(x, y, z), (x + dx, y, z), (x + dx, y + dy, z), (x, y + dy, z),
         (x, y, z + dz), (x + dx, y, z + dz), (x + dx, y + dy, z + dz), (x, y + dy, z + dz)]
    faces = [(0, 1, 2, 3, (0, 0, -1)), (4, 7, 6, 5, (0, 0, 1)),
             (0, 4, 5, 1, (0, -1, 0)), (1, 5, 6, 2, (1, 0, 0)),
             (2, 6, 7, 3, (0, 1, 0)), (3, 7, 4, 0, (-1, 0, 0))]
    tris = []
    for a, b, c, d, n in faces:
        tris.append((p[a], p[b], p[c], n))
        tris.append((p[a], p[c], p[d], n))
    return tris


def baue(name):
    name = name.upper()
    cols = max(1, len(name) * 6 - 1)
    breite = cols * PX + 2 * RAND
    hoehe = 7 * PX + 2 * RAND
    tris = _box(0, 0, 0, breite, hoehe, BASE)         # Grundplatte
    for i, ch in enumerate(name):
        g = FONT.get(ch, FONT[" "])
        ox = RAND + i * 6 * PX
        for r, zeile in enumerate(g):
            for c, pixel in enumerate(zeile):
                if pixel == "#":
                    x = ox + c * PX
                    y = RAND + (6 - r) * PX            # Zeile 0 = oben
                    tris += _box(x, y, BASE - SINK, PX, PX, RAISE + SINK)
    return tris


def schreibe_stl(tris, pfad, name="namensschild"):
    with open(pfad, "w") as f:
        f.write(f"solid {name}\n")
        for a, b, c, n in tris:
            f.write(f"  facet normal {n[0]} {n[1]} {n[2]}\n    outer loop\n")
            for v in (a, b, c):
                f.write(f"      vertex {v[0]:.3f} {v[1]:.3f} {v[2]:.3f}\n")
            f.write("    endloop\n  endfacet\n")
        f.write(f"endsolid {name}\n")


def main():
    namen = sys.argv[1:] or ["NAME"]
    for name in namen:
        tris = baue(name)
        datei = f"namensschild_{name.upper().replace(' ', '-')}.stl"
        schreibe_stl(tris, datei)
        print(f"geschrieben: {datei}  ({len(tris)} Dreiecke)")


if __name__ == "__main__":
    main()
